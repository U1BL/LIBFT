#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*tmp;

	i = 0;
	tmp = (char *)s;
	// if (n == 0)
	// 	return (NULL);
	while (i < (int)n)
	{
		tmp[i] = 0;
		i++;
	}
}